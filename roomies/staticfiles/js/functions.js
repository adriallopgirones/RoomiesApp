function display_paied_purchases() {
  var x = document.getElementById("display-paied-purchases");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}

function display_debt_table() {
  var x = document.getElementById("display-debt-table");
  if (x.style.display === "none") {
    x.style.display = "block";
  } else {
    x.style.display = "none";
  }
}